The Moving Picture Experts Group (MPEG) has developed several video coding standards over the years. Here are the main MPEG video algorithms:

1. **MPEG-1**: Designed for coding of moving pictures and associated audio for digital storage media at up to about 1.5 Mbit/s¹.
2. **MPEG-2**: Used for digital television, including satellite TV, cable TV, and DVDs. It supports higher bit rates and interlaced video¹.
3. **MPEG-4**: Includes a wide range of features for multimedia applications, such as video streaming, video conferencing, and digital television².
4. **MPEG-7**: Focuses on multimedia content description, allowing for efficient searching, indexing, and retrieval of multimedia content².
5. **MPEG-21**: Aims to define an open framework for multimedia delivery and consumption, including digital rights management².

Additionally, there are more recent standards like:
- **AVC (Advanced Video Coding)**, also known as H.264³.
- **HEVC (High Efficiency Video Coding)**, also known as H.265³.
- **LCEVC (Low Complexity Enhancement Video Coding)**³.
- **VVC (Versatile Video Coding)**, also known as H.266³.

These standards have evolved to provide better compression efficiency, higher quality, and support for various applications and devices.

